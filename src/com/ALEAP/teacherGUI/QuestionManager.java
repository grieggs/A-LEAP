    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        if(isNew){
            if(mcButton.isSelected()){
                String[] ans = {this.ansBox2.getText(),this.ansBox3.getText(),this.ansBox4.getText()};
                MultipleChoiceQuestion out = new MultipleChoiceQuestion(0, (Integer)this.difSpin.getValue(), this.questionText.getText(), this.ansBox1.getText(), ans, 0, 0);
                this.activeT.addQuestion(out);
                parent.updateList();
                this.dispose();
            }else{
                Question out = new Question(0, (Integer)this.difSpin.getValue(), this.questionText.getText(), this.ansBox1.getText(), 0, 0);
                this.activeT.addQuestion(out);
                parent.updateList();
                this.dispose();
            }
        }else{
            if(mcButton.isSelected()){
                String[] ans = {this.ansBox2.getText(),this.ansBox3.getText(),this.ansBox4.getText()};
                ((MultipleChoiceQuestion)active).setOptions(ans);
                this.active.setText(this.questionText.getText());
                this.active.setDifficulty((Integer)this.difSpin.getValue());
                this.active.setCorrect(this.ansBox1.getText());
                parent.updateList();
                this.dispose();
            }else{
                this.active.setText(this.questionText.getText());
                this.active.setDifficulty((Integer)this.difSpin.getValue());
                this.active.setCorrect(this.ansBox1.getText());
                parent.updateList();
                this.dispose();
            }
        }
    }//GEN-LAST:event_submitButtonActionPerformed

