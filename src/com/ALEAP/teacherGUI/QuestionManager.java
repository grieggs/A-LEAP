    public void editQuestion(Question newQ, Topic top){
        this.isNew = false;
        this.activeT = top;
        this.active = newQ;
        if(active.getType().equals("MC")){
            this.mcButton.setSelected(true);
            this.mcButton.setEnabled(false);
            this.saButton.setEnabled(false);
            String[] ans = ((MultipleChoiceQuestion)this.active).getOptions();
            this.ansBox2.setText(ans[0]);
            this.ansBox3.setText(ans[1]);
            this.ansBox4.setText(ans[2]);
        }else{
            this.saButton.setSelected(true);
            this.mcButton.setEnabled(false);
            this.saButton.setEnabled(false);
            this.ansBox2.setEnabled(false);
            this.ansBox3.setEnabled(false);
            this.ansBox4.setEnabled(false);
        }
        this.questionText.setText(active.getText());
        this.ansBox1.setText(active.getCorrect());
        this.repaint();
    }
    
    public void newQuestion(Topic top){
        this.isNew = true;
        this.activeT = top;
    }
    
